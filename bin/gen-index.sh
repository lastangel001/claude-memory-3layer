#!/usr/bin/env bash
# gen-index.sh — regenerate the .claude-docs/index.md routing table from each
# doc's `description:` frontmatter. Hand-written notes in the MANUAL block are
# preserved; only the region between AUTO-INDEX markers is rewritten.
#
# A doc with no `description:` falls back to its first `# ` heading, then its
# filename. index.md itself is never listed.
#
# Usage:
#   bash ~/.claude/bin/gen-index.sh              # regenerate index.md in cwd repo
#   bash ~/.claude/bin/gen-index.sh --dir PATH   # explicit .claude-docs dir
#   bash ~/.claude/bin/gen-index.sh --check      # non-zero exit if out of date
#   bash ~/.claude/bin/gen-index.sh --force      # scaffold even over a markerless index.md
#
# An existing index.md WITHOUT AUTO-INDEX markers is never clobbered — the
# script refuses and tells you which two marker lines to add (or use --force).
#
# Exit: 0 on success; with --check, 1 if index.md would change.

set -euo pipefail

AUTO_START='<!-- AUTO-INDEX:START — generated by bin/gen-index.sh; edits here are overwritten -->'
AUTO_END='<!-- AUTO-INDEX:END -->'
MANUAL_MARK='<!-- MANUAL: hand-written routing notes below are preserved across regeneration -->'

docs_dir=""
check_only=0
force=0
while [[ $# -gt 0 ]]; do
  case "$1" in
    --dir)   docs_dir="$2"; shift 2 ;;
    --check) check_only=1; shift ;;
    --force) force=1; shift ;;
    -h|--help) grep -E '^#( |$)' "$0" | sed 's/^# \{0,1\}//'; exit 0 ;;
    *) printf 'Unknown arg: %s\n' "$1" >&2; exit 1 ;;
  esac
done

if [[ -z "$docs_dir" ]]; then
  if git rev-parse --show-toplevel >/dev/null 2>&1; then
    docs_dir="$(git rev-parse --show-toplevel)/.claude-docs"
  else
    docs_dir="$PWD/.claude-docs"
  fi
fi
[[ -d "$docs_dir" ]] || { printf 'Error: no .claude-docs dir: %s\n' "$docs_dir" >&2; exit 1; }

index="$docs_dir/index.md"

# Frontmatter scalar (tolerates indexer nesting under metadata:).
_fm_get() { sed -n "s/^[[:space:]]*$2:[[:space:]]*//p" "$1" 2>/dev/null | head -n1 | tr -d '\r'; }

# Build the auto rows, sorted by filename for stability.
rows=""
for md in $(printf '%s\n' "$docs_dir"/*.md | sort); do
  [[ -f "$md" ]] || continue
  base=$(basename "$md")
  [[ "$base" == "index.md" ]] && continue
  desc=$(_fm_get "$md" description)
  if [[ -z "$desc" ]]; then
    desc=$(grep -m1 '^# ' "$md" 2>/dev/null | sed 's/^# *//' | tr -d '\r' || true)
  fi
  [[ -z "$desc" ]] && desc="$base"
  rows+="| [${base}](${base}) | ${desc} |"$'\n'
done

auto_block="${AUTO_START}
| Doc | What it covers |
|---|---|
${rows}${AUTO_END}"

# Compose the new index.md. Preserve frontmatter + header + manual block if the
# file already exists; otherwise scaffold a fresh one.
tmp="$(mktemp)"
if [[ -f "$index" ]] && grep -qF "$AUTO_START" "$index" && grep -qF "$AUTO_END" "$index"; then
  # Replace only the marked region; keep everything before START and after END.
  awk -v start="$AUTO_START" -v end="$AUTO_END" -v block="$auto_block" '
    $0 == start {print block; skip=1; next}
    $0 == end   {skip=0; next}
    !skip {print}
  ' "$index" > "$tmp"
elif [[ -f "$index" && $force -eq 0 ]]; then
  # Existing hand-written index.md with no markers — refuse to clobber.
  rm -f "$tmp"
  if [[ $check_only -eq 1 ]]; then
    printf '✗ index.md has no AUTO-INDEX markers — add them (or run with --force)\n' >&2
    exit 1
  fi
  {
    printf 'index.md exists but has no AUTO-INDEX markers — not overwriting.\n'
    printf 'Add these two lines where the generated table should go, then re-run:\n'
    printf '  %s\n  %s\n' "$AUTO_START" "$AUTO_END"
    printf 'Or pass --force to replace the file with a fresh scaffold.\n'
  } >&2
  exit 1
else
  # Fresh scaffold (index.md absent, or --force).
  {
    printf -- '---\ntags: [memory/repo, index]\ndescription: Routing table — start here when unsure which doc to read\n---\n\n'
    printf '# .claude-docs index — %s\n\n' "$(basename "$(dirname "$docs_dir")")"
    printf 'Routing table. Start here when unsure which doc to read.\n\n'
    printf '%s\n\n' "$auto_block"
    printf '%s\n' "$MANUAL_MARK"
  } > "$tmp"
fi

if [[ $check_only -eq 1 ]]; then
  if [[ -f "$index" ]] && cmp -s "$tmp" "$index"; then
    rm -f "$tmp"; printf '✓ index.md up to date\n'; exit 0
  else
    rm -f "$tmp"; printf '✗ index.md out of date — run: bash bin/gen-index.sh\n' >&2; exit 1
  fi
fi

mv "$tmp" "$index"
printf '✓ regenerated %s\n' "$index"
